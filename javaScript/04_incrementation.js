/*L'incrémentation est l'opération qui consiste à ajouter
1(et par extension toute valeur entière fixée)
à un compteur.L'opération inverse est la décrementation
Elle consite à retirer 1 au compteur.*/
//L'opérateur d'incrément ajoute une unité à son opérande
//et renvoie une valeur.
//-----INCREMENTATION-------//
var nb1 = 1;
nb1 = nb1 +1; //j'augmente nb1 de 1
console.log(nb1);//affichage => nb1=2
//-----ECRITURE SIMPLIFIEE----//
nb1++
console.log(nb1);
//affichage => nb1=3
//-----ECRITURE SIMPLIFIEE---//
++nb1;
console.log(nb1);
//affichage => nb1 = 4

//-------DECREMENTATION------//
nb1 = nb1 - 1;
console.log(nb1);
//----ECRITURE SIMPLIFIEE----//
nb1--;
console.log(nb1);
//affichage nb1 => 2
